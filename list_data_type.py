# list data type 
#1. insertion order is preserved

list = [ 10, 10.5, "gwg", True, 10 ]
print(list[0])
print(list) #[10, 10.5, 'gwg', True, 10]

print(list)

list.append("Agra") #[10, 10.5, 'gwg', True, 10, 'Agra']

print(list)

list.remove("gwg") #[10, 10.5, True, 10, 'Agra']

print(list)

