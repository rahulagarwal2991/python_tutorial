# Membership operator
    # in 
    # not in

string1 = "hello this is my python class"
print("a" in string1) # True
print("g" in string1) # False

print("g" not in string1) #True
print("e" not in string1) #False

print("this" in string1) #True

list1 = ["one", "two", "three"]
print("one" in list1) #True
print("four" in list1) #False